--- 7. What is the average total historical deposit counts and amounts for all customers?

WITH customer_deposits AS (
    SELECT 
        customer_id,
        COUNT(*) AS deposit_count,
        SUM(txn_amount) AS total_amount
    FROM customer_transactions
    WHERE txn_type = 'deposit'
    GROUP BY customer_id
)
SELECT 
    AVG(deposit_count) AS avg_deposit_count,
    AVG(total_amount) AS avg_deposit_amount
FROM customer_deposits;


--- 8. For each month how many Data Bank customers make more than 1 deposit and either 1 purchase or 1 withdrawal in a single month?

WITH monthly_txns AS (
    SELECT 
        customer_id,
        FORMAT(txn_date, 'yyyy-MM') AS month,
        SUM(CASE WHEN txn_type = 'deposit' THEN 1 ELSE 0 END) AS deposits,
        SUM(CASE WHEN txn_type = 'purchase' THEN 1 ELSE 0 END) AS purchases,
        SUM(CASE WHEN txn_type = 'withdrawal' THEN 1 ELSE 0 END) AS withdrawals
    FROM customer_transactions
    GROUP BY customer_id, FORMAT(txn_date, 'yyyy-MM')
)
SELECT 
    month,
    COUNT(DISTINCT customer_id) AS customers
FROM monthly_txns
WHERE deposits > 1
  AND (purchases >= 1 OR withdrawals >= 1)
GROUP BY month;



--- 9. What is the closing balance for each customer at the end of the month?


WITH running_balance AS (
    SELECT
        customer_id,
        txn_date,
        DATEFROMPARTS(YEAR(txn_date), MONTH(txn_date), 1) AS month_start,
        SUM(txn_amount) OVER (
            PARTITION BY customer_id
            ORDER BY txn_date
            ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
        ) AS closing_balance
    FROM customer_transactions
),

month_end_balance AS (
    SELECT
        customer_id,
        month_start,
        closing_balance,
        ROW_NUMBER() OVER (
            PARTITION BY customer_id, month_start
            ORDER BY txn_date DESC
        ) AS rn
    FROM running_balance
)

SELECT
    customer_id,
    month_start,
    closing_balance
FROM month_end_balance
WHERE rn = 1
ORDER BY customer_id, month_start;



--- 10. What is the percentage of customers who increase their closing balance by more than 5%?


WITH monthly_balance AS (
    SELECT
        customer_id,
        DATEFROMPARTS(YEAR(txn_date), MONTH(txn_date), 1) AS month_start,
        SUM(txn_amount) OVER (
            PARTITION BY customer_id 
            ORDER BY txn_date
        ) AS closing_balance
    FROM customer_transactions
),

balance_with_prev AS (
    SELECT
        customer_id,
        month_start,
        closing_balance,
        LAG(closing_balance) OVER (
            PARTITION BY customer_id 
            ORDER BY month_start
        ) AS prev_balance
    FROM monthly_balance
),

growth AS (
    SELECT
        customer_id,
        month_start,
        CASE 
            WHEN prev_balance IS NOT NULL AND prev_balance > 0 THEN
                (closing_balance - prev_balance) * 1.0 / prev_balance
        END AS growth_rate
    FROM balance_with_prev
)

SELECT 
    ROUND(
        100.0 * COUNT(DISTINCT CASE WHEN growth_rate > 0.05 THEN customer_id END)
        / COUNT(DISTINCT customer_id),
    2) AS percentage_of_customers
FROM growth;
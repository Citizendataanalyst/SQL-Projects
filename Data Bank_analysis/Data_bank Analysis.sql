  --- Business Questions

 --- 1. How many unique nodes are there on the Data Bank system?

 SELECT COUNT(DISTINCT node_id) AS unique_nodes
FROM customer_nodes;



--- 2. What is the number of nodes per region?

SELECT 
    region_id,
    COUNT(DISTINCT node_id) AS nodes_per_region
FROM customer_nodes
GROUP BY region_id;




--- 3. How many customers are allocated to each region?

SELECT 
    region_id,
    COUNT(DISTINCT customer_id) AS customers
FROM customer_nodes
GROUP BY region_id;




--- 4. How many days on average are customers reallocated to a different node?

SELECT 
    AVG(DATEDIFF(DAY, start_date, end_date)) AS avg_reallocation_days
FROM customer_nodes
WHERE end_date IS NOT NULL;




--- 5. What is the median, 80th and 95th percentile for this same reallocation days metric for each region?

WITH reallocation AS (
    SELECT 
        region_id,
        DATEDIFF(DAY, start_date, end_date) AS days_diff
    FROM customer_nodes
    WHERE end_date IS NOT NULL
)
SELECT DISTINCT
    region_id,
    PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY days_diff) 
        OVER (PARTITION BY region_id) AS median_days,
    PERCENTILE_CONT(0.8) WITHIN GROUP (ORDER BY days_diff) 
        OVER (PARTITION BY region_id) AS p80_days,
    PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY days_diff) 
        OVER (PARTITION BY region_id) AS p95_days
FROM reallocation;



